        <h1>Research Interests</h1>

        <p>    Dakouri-Hild's specialty is Aegean prehistory within the broader framework of prehistoric societies and material cultures of the Old World. Her research interests include Boeotian and Theban archaeology, cognition and material/visual culture, multisensory approaches to studying the past, phenomenological approaches to space, performance and the politics of identity, ceramic analysis and technology, social aspects of technology and technique, digital technologies in heritage management (Geographic Information Systems, network analysis, databases), and instructional technology and pedagogy. In her doctoral thesis she addressed the interrelationship of value, meaning and style and the construction of corporate identities through material culture in prehistoric Thebes.</p>

