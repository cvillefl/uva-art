        <p class="small">Anastasia Dakouri-Hild teaches cultural history of Aegean and Near Eastern art and architecture from a theoretical and contextual perspective, showing a particular interest in the role of visual culture in shaping belief and the politics of identity, the dynamic interaction of materiality and cognition, and the relevance of the past in present society. 
         
        <p class="small">An active field archaeologist in Greece, and a former employee of the Archaeological Service (heritage management and rescue excavations), she has collaborated with the Service on various projects, including the re-excavation and final publication of the House of Kadmos in Late Bronze Age Thebes and, more recently, the republication of the prehistoric chamber tomb cemeteries in the vicinity of the Theban citadel.
         
        <p class="small">Dakouri-Hild has published extensively on Thebes and Boeotia in the Late Bronze Age. Her work on the House of Kadmos has earned her the prestigious Michael Ventris Award (Institute of Classical Studies, University of London; 2001), a three-year grant from Harvard University (Semitic Museum, 2001-2003) and a year-long faculty fellowship from the American Council of Learned Societies (2008-2009). 
