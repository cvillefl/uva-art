		<h1>Majors and Minors</h1>

		<p>
			Major requirements are designed to maximize exposure to a range of world art traditions, while allowing students to develop and refine their study of specific areas of interest. Working in dialogue with faculty advisers, majors plan programs that combine introductory lecture courses with smaller, more specialized colloquia and seminars. Foreign travel and education abroad can provide unparalleled opportunities for students to refine and extend their study of art, architecture, and archaeology. Department faculty enthusiastically encourage foreign study, and advisers work closely with students to integrate rigorous and rewarding study abroad experiences into their major.
		</p>

		<p>
			<strong>Major requirements</strong>
			For a degree in art history, students must complete 11 courses in the department, 10 of which must be above the 
			1000 level. Courses taken at any time during the student's career can be counted, including those earned while 
			studying abroad, in summer session, or in architectural history. By graduation, a student must have achieved a 
			 <acronym>gpa</acronym> of 2.000 in major courses. A student who does not maintain an average of 2.000 or better in departmental 
			 courses will be put on probation, and may be dropped from the program. No course graded below C- may count 
			 for major credit.
		</p>	 

		<p>
			<strong>Distribution Requirements</strong>
			At least one course at the 2000 level or above in each of five areas (Ancient, Medieval, Renaissance/Baroque, 
			Modern, Non-Western); a minimum of two 4000 level seminars (either one <acronym>arth</acronym> 4051 and one <acronym>arth</acronym> 4591, or 
			two <acronym>arth</acronym> 4591); and three electives within the department. One class at the 1000-level may be counted 
			as elective credit. At least two of the non-seminar courses must be at the 3000 or 5000 level. 
			Requirements may be fulfilled by appropriate courses in Architectural History at the 2000 level or 
			above. One course in Arts Administration (<acronym>arad</acronym>) and one course in Studio Art at the 1000 level or 
			above may count as electives.
		</p>	

		<p>
			<i>Advanced Placement (<acronym>ap</acronym>)/ International Baccalaureate (<acronym>ib</acronym>) courses:</i><br>
			One Advanced Placement (<acronym>ap</acronym>) Art History course with a score of 5 or International Baccalaureate (<acronym>ib</acronym>) Art History 
			course with a grade of 6 or 7 may be applied toward the major. The course will transfer as either <acronym>arth</acronym> 1051 
			or 1052 and will count as elective credit.
		</p>

		<p>		 
			<strong>Minor requirments</strong>
			There are no prerequisites for a minor in art history. A student must complete 15 credits in the department, 
			beyond the 1000 level. Courses taken at any time during the student's career may be counted toward the 
			minor, including those earned while studying abroad or in summer session. At the time of graduation, a 
			student must have achieved a minimum <acronym>gpa</acronym> of 2.0 in the minor courses.
		</p>	 

		<p>
			<strong>Distrution Requirements</strong>
			Minors must take at least one course in four of the five areas: Ancient, Medieval, Renaissance/Baroque, 
			Modern, and Non-Western. At least one course must be at the 3000-level or higher. One of the requirements 
			may be fulfilled by an appropriate course in Architectural History at the 2000-level or above.
		</p>
 
