		<h1>About the program</h1>

		<p>
			The undergraduate program in art history equips students with the fundamental skills and perspectives of the liberal 
			arts: to think clearly and critically, to write well, and to find, analyze, evaluate, and present facts and ideas. The Art
			History faculty is committed to training students to become exceptional thinkers and writers, as well as sophisticated 
			interpreters of visual evidence. Students develop rigorous historical understanding of the circumstances and cultural 
			settings in which art has been created and viewed in a curriculum that spans the ancient Mediterranean; South and 
			East Asia; the Italian and Northern Renaissance; medieval, modern, and contemporary Europe; and the United 
			States. Faculty research and teaching introduces students to an expansive range of media: from painting, sculpture, 
			prints and drawings, photography and film, to archaeology, architecture, urbanism, and material culture.
		</p>

		<p>
			Student learning is animated and amplified by firsthand encounters with objects in the collection of the Fralin 
			Museum, in University Library Special Collections, and by the extraordinary riches of the University's architectural 
			landscape. Art History offices and seminar rooms are located in Fayerweather Hall, where faculty and students 
			enjoy close proximity to the Fralin Museum, Ruffin Hall and the Studio Art program, the Fine Arts Library, and the 
			School of Architecture. Majors benefit from this vibrant community of contemporary art and architecture, and many 
			take courses in studio art and architectural history.
		</p>

		<p>
			The undergraduate program is designed both to prepare students for advanced graduate study in the history of
			art and to provide students with a broad, humanistic background for many other fields that require the ability to 
			undertake independent research, evaluate visual and textual evidence, to write effectively, and create intelligent 
			arguments. Our majors have gone on to thrive in graduate MA and PhD programs in art history and in careers 
			in College and University teaching, museums, and the art market; for others, art history coursework has proven 
			outstanding preparation for a broad range of disciplines, including law, business, and medicine.
		</p>
