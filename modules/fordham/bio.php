        <p class="small">    Douglas Fordham teaches, lectures, and publishes widely on eighteenth-century visual culture. He is the author of <i>British Art and the Seven Years' War: Allegiance and
         Autonomy</i> (University of Pennsylvania Press, 2010) and a co-editor with Tim Barringer and Geoff Quilley of <i>Art and the British Empire</i> (Manchester University Press, 2007). Common themes running through his publications include the impact of political contingency on artistic form, the role of empire and globalization in the formation of Western art, and the relationship between representation and ecology. 
 
        <p class="small">Fordham earned his doctorate at Yale University in 2003 under the direction of Tim Barringer. He then worked as a Mellon Postdoctoral Fellow and Lecturer at Columbia University until 2005 when he joined the faculty of the University of Virginia. He has received research and publication grants from the Paul Mellon Centre in London, the Rare Book School in Charlottesville, and the Folger Institute in Washington, D.C.
 
        <p class="small">In addition to scholarly conferences, Fordham has had the pleasure of delivering public lectures at the Metropolitan Museum in New York, the British Museum in London, the National Gallery of Victoria in Melbourne, and the Yale Center for British Art in New Haven, CT. 
        </p>

