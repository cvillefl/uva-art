        <h1>Projects</h1>

        <h2>    British Art and the World in 1800</h2>
 
        <p>Examining the work of British artists in Western India, China, and Southern Africa in the decades around 1800, this book ventures a comparative analysis of three unique intercultural encounters. Key Enlightenment concepts including cosmopolitanism, mercantilism, and naturalism help to structure artistic ambitions in these disparate locales even as the universal pretensions underpinning these concepts falter time and again in artistic practice. 

        <h2>James Gillray catalogue raisonn&eacute;</h2>
         
        <p>I am currently in discussions with colleagues and institutions about the possibility of creating an online catalogue of James Gillray's drawings, satirical prints, and distinctive print states that could be collaboratively tagged and coded for use by scholars around the world. 

        <h2>Artistic Subjectivity and Legal Subjecthood</h2>
         
        <p>To what extent was artistic subjectivity shaped by legal subjecthood (including citizenship, copyright protection, and speech protections) in early modern Britain? This long-term research project attempts to map two disparate historiographies--one legal and the other art-historical-- onto a single narrative about Crown subjects as artistic agents.   

