				<a href="#">Fordham ></a>
				TTR 2:00 - 3:15 FHL 208
			</div>

			<div class="clr"></div>

			<p class="small">
				This introductory seminar will explore the enduring theme of warfare in the visual arts across a wide swath of times and cultures. The class will examine a variety of media including paintings, prints, sculpture, and film that range from the celebratory to the deeply critical. The class will also consider how warfare shaped fine art's relationship to the state and the concept of national culture more generally.
