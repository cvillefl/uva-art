		<h2 id="majors">Studio Art Majors</h2>

		<p>Majors acquire essential artistic skills as well as experience in the 
		handling of a wide variety of ideas, materials and methods, putting student in touch with the problems of creation and with the ideas and practice of artists in the contemporary world. The Studio major requires 
		a concentration in  one of the areas of Cinematography, New Media, Painting, Photography, Printmaking, or Sculpture, and it culminates in a thesis exhibition in the spring of the fourth year. 

		<p>The minimum 30 credits required for the major can be broken down as follows:</p>

		<ul>
			<li><i class="fa fa-stop"></i> 3 credits <acronym>arts</acronym> 2610 Drawing (formerly <acronym>arts</acronym> 1610)</li>
			<li><i class="fa fa-stop"></i> 3 credits <acronym>arth</acronym> 2471 Art Since 1945</li>
			<li><i class="fa fa-stop"></i> 15 <acronym>arts</acronym> credits at the 2000 level, including intro sequence<span>of concentration.</span></li>
			<li><i class="fa fa-stop"></i> 3 <acronym>arts</acronym> credits at 3000 level in concentration</li>
			<li><i class="fa fa-stop"></i> 3 <acronym>arts</acronym> credits at 4000 level in concentration</li>
			<li><i class="fa fa-stop"></i> 3 elective credits (<acronym>arts</acronym> or <acronym>arth</acronym>)</li>
		</ul>

		<p><acronym>arts</acronym> 2610 is required for entry into all upper level studio courses (note that students should not take both courses). <acronym>arth</acronym> 2471 is required for the major and should be taken in the fall term of the third year. Painting, Printmaking, and Sculpture courses require <acronym>arts</acronym> 2620 (Drawing II) or figure drawing as a prerequisite to all classes.</p>

		<p>The minimum requirements for a concentration are the 2000 level intro courses in the chosen concentration area along with one 3000 level and one 4000 level course in that same area. The 4000 level course will be taken in the spring of the Fourth year during which the student will develop his or her thesis exhibition.</p>

		<p>Nine additional credits at the 2000 level are required for the major and at least one of those classes must be in an area other than the student's concentration. </p>

		<p>An elective is also required for the Studio Art Major and consists of either an additional <acronym>arth</acronym> course, an <acronym>arts</acronym> Special Topics course, or an additional course at the 3000/4000 level.</p>

		<p>Students who begin their concentration in their second year may choose to take up to six classes in their area, including the intro courses, two courses at the 3000 level, and two courses at the 4000 level.</p>

		<p>Please note: <acronym>arts</acronym> 4900 (Advanced Project in Art) does not count toward major credit.</p>

		<p>Majors must have a minimum gpa of 2.000 in all 30 credits of Studio Major courses, or be dropped from the program. A grade of C- or below does not count for major credit. With approval of the Director of the Undergraduate Program, students may transfer up to 9 credits to the major.</p>

