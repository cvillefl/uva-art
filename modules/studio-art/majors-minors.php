		<h2 id="minors">Requirements for Minors</h2>

		<p>The minor in studio art requires 18 credits in <acronym>arts</acronym> courses including <acronym>arts</acronym> 2610 (formerly arts 1610). 15 credits of <acronym>arts</acronym> 2000 level or above courses in at least two concentration areas. <acronym>arts</acronym> 4900 (Advanced Project in Art) does not count toward minor in Studio Art.</p>
 
		<p>Minors must have a minimum <acronym>gpa</acronym> of 2.000 in All 18 credits of Studio Minor courses. No <acronym>arts</acronym> course graded C- or below may count for minor credit. With approval of the Director of the Undergraduate Program, students may transfer up to 6 credits to the minor.</p>
		 
