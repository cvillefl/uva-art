		<h1>International Programs</h1>

		<h2>Studio Summer Photography Program in Italy</h2>

		<p>Every other summer the Studio Department offers a unique photography course in Italy through the Study Abroad Office at UVA. The course begins in Rome before moving to a base in central Tuscany and eventually visits Siena, Florence and many other cities. During their time in Tuscany, the student's room and board is provided by the Santa Chiara Study Center, on the edge of a small, picturesque hilltop town. The course is offered concurrently with a Contemporary Art History class to maximize student's experience of the culture and art and culminates with a four-day visit to the Venice Biennale Art Festival. Through class work and field trips, students experience the important art sites of the region and create new work in response to the exceptional situation.</p>

		<p>This study abroad program allows students to earn six UVA art credits and UVA grades, not simply transfer credit. These credits count towards UVA graduation requirements. In addition, all courses offered in Italy can count for Studio Art major requirements in the College of Arts and Sciences.
