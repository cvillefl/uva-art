		<h1>Studio Safety</h1>

		
		<h2>Introduction</h2>

		<p>Each member of the Art Department, whether faculty, staff, or student, is responsible for knowing and observing University of Virginia Environmental Health and Safety (UVA-EHS) and the McIntire Department of Art, Studio Art Environmental Health and Safety (SA-EHS) Policies. The goal is to develop positive attitudes regarding health and safety among all faculty, staff, and students within the studio program. It is essential that all members take an active part to initiate and enforce preventive measures to control hazards associated with activities under their direction.</p>

		<p>The purpose of this policy is to set forth general guidelines for the Studio Art Department as Individual Studio (Area-Specific) Safety Procedures. Individual studio safety procedures are essential in providing a safe and healthful performance of art activities and the safest possible working and educational environment for faculty, staff, and students. The Art Department Individual Studio Safety Procedures will follow the provisions of the University EHS Program and any other applicable local, state, and/or federal regulations.</p>
		 
		<blockquote class="m"><a href="#" target="_blank">The Studio Art Department Safety Procedures ></a> (forthcoming)</blockquote>

		<h2>Goals</h2>

		<p>The goal of the Art Department is to create a safe working environment through the development of:</p>

		<ul class="no_top_margin">
			<li><i class="fa fa-stop"></i> Safety instruction</li>
			<li><i class="fa fa-stop"></i> General and area specific written procedures</li>
			<li><i class="fa fa-stop"></i> Supervision</li>
			<li><i class="fa fa-stop"></i> Training in the use of materials and equipment</li>
		</ul>

		<h2>Dissemination of safety information</h2>

		<p>The overall responsibility for safety and health rests with the 
		supervisor. Faculty members are responsible for safety and 
		housekeeping in their teaching areas. Where two faculty members share a facility, the supervisor shall designate areas of responsibility. Each faculty member is responsible for developing, posting, and enforcing any special safety precautions particular to his/her area arising from the nature of instruction. Each student is responsible 
		for knowing all school and area-specific safety policies. Assistance in assuring compliance will be provided by Safety Supervisor, 
		<a href="mailto:ems3z@virginia.edu">Eric Schmidt ></a></p>
		
