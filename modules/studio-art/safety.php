		<h2 class="rhc_head">Responsibilities</h2>

		<p>Responsibilities for the University and Art Department Environmental Health and Safety Program are:</p>
 
		<p>
			<strong>Faculty and teaching assistants</strong>
			Each faculty member and teaching assistant is responsible for the dissemination of information to students and employees under their active academic jurisdiction. These responsibilities include, but are not limited to, the following:
		</p>

		<ul class="no_top_margin">
			<li>
				<i class="fa fa-stop"></i> Explain to students the University and Studio Art Department 
			    <span class="line2">safety regulations/procedures pertinent to their specific 
			    academic tasks and/or activities</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Assure the safe and appropriate use and storage <span class="line2">of 
			    materials in the lab/studio areas</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Require students to use personal protective devices and 
			    <span class="line2">clothing as needed for the proposed instruction or activity. 
			    Such devices and equipment must be maintained in 
			    good repair.</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Inspect instructional areas frequently for identification and 
			    <span class="line2">prompt elimination of unsafe practices and conditions. 
			    Advice and assistance is available from Environmental 
			    Health and Safety on campus, and from the Safety 
			    Supervisor</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Seek prompt medical treatment for any student injured <span class="line2">by 
			    calling 9-911</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Notify the Safety Supervisor and Department Chair of all 
			    <span class="line2">accidents involving students, faculty, or staff, even those 
			    that do not require medical attention. This information is 
			    used statistically to plan future safety policies</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Submit recommendations for the improvement of the 
			    <span class="line2">immediate academic environment to the Safety 
			    Coordinator or Department Chair</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Assure compliance with the Hazard Communication Act of 
			    <span class="line2">Virginia. This Act (The Right to Know Law) includes three 
			    areas of responsibility:</span>
			    <ul class="no_top_margin">
			    	<li><i class="orange">-</i> Maintain Safety Data Sheets (SDS) on file and <span class="line2b">available in the work area(s)</span></li>
		    		<li><i class="orange">-</i> Label hazardous chemicals or products</li>
		    		<li><i class="orange">-</i> Written Hazardous Communication Program and <span class="line2b">training Assistance in assuring compliance will be provided by the Safety Supervisor</span></li>
	            </ul>
	        </li>
		</ul>


		<p>
			<strong>Staff, Student Employees, and Students</strong>
			University employees and students are subject to all campus health and safety regulations. Compliance is vital to the creation and maintenance of a healthy and safe campus environment. These responsibilities include, but are not limited to, the following:
		</p>


		<ul class="no_top_margin">
			<li>
				<i class="fa fa-stop"></i> Understand and comply with the University and Studio
			    <span class="line2">Department Safety Instructions, whether written or oral, 
			    when performing assigned work duties</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Use only tools and equipment approved or provided by the 
	    		<span class="line2">supervisor/instructor</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Use appropriate safety equipment and guards, and work 
	    		<span class="line2">with established safety procedures</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Report unsafe conditions, practices or equipment to the 
	    		<span class="line2">instruction/supervisor or Safety Supervisor whenever such 
	    		deficiencies are observed and as often as necessary to 
	    		assure correction</span>
			</li>
			<li>
				<i class="fa fa-stop"></i> Inform the instructor/supervisor and safety supervisor 
	    		<span class="line2">immediately of all injuries or accidents and assist injured 
	    		persons in obtaining prompt medical treatment when 
	    		necessary-by informing the Safety Supervisor and/or using 
	    		one of the emergency phones</span>
	    	</li>
		</ul>

