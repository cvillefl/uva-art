		<h1>Martha Jefferson Starr Hill Health Center</h1>
		Programs offered by The Martha Jefferson Starr Hill Health Center focus on improving the health of children, teens and families in the City of Charlottesville. Overweight and obesity are particular problems in our community that are risk factors for heart disease, stroke, diabetes, asthma, and certains types of cancer. In an effort to create a healthier community the Center's Nurse Practitioner designs individual, step by step plans while the Hospital's Community Outreach Office, also 
		located at the Starr Hill Center engages the community. Services are free of charge. 
