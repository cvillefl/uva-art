		<h1>Literacy Volunteers</h1> 
		Literacy Volunteers of Charlottesville/Albemarle uses over 200 volunteer tutors each year to help adults improve the reading, writing, math, and English speaking skills of approximately 275 adults. Through our one-to-one tutoring sessions and small group lessons, we help our city's residents improve their standard of living by making them eligible for job-training programs and continuing education. Approximately 9000 residents in our community need our services, and the population of adults with a limited English proficiency is growing. Gaining the ability to read, write, and speak English enables our students to become self-sufficient, productive citizens in our community. 
