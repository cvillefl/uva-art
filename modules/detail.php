		<h1>Piedmont Virginia Community College </h1>
		Since January 2013 when the Jefferson School City Center opened to the public, hundreds of PVCC students have taken general education credit classes, such as English, history, math, psychology, information technology, business, religion and sociology, which are offered in PVCC's Jefferson School Center on the first floor of the building. PVCC's space consists of four state-of-the-art classrooms, offices, a 2,500-square-foot commercial-grade teaching kitchen and a 700-square-foot culinary classroom. Opening of the PVCC Jefferson School Center enabled the College to fulfill a goal of launching a new Associate of Applied Science degree in Culinary Arts, starting with the fall 2013 semester. Forty students are currently enrolled in this program of study that prepares them for careers in the foodservice industry. More than 100 students have enrolled in an array of PVCC noncredit cooking classes also offered at this Center. These are taught by chefs and hospitality experts from the Charlottesville area. 
