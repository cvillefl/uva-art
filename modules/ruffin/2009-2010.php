<div class="span50" id="boberska">
    <h2>Berenika Boberska | Fallow City Project</h2>

    <p class="small">
        <b>February 26 - March 26, 2010</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Boberska-Fallow_City.pdf" target="_blank">Download announcement (pdf) ></a>
    </p>

        <p>Fallow CIty is an intervention in the typical fabric of suburbia, in the monoculture of private spaces and cultivated dreams. Taking the current crisis of the suburbs as a chance, and the most extreme situation of Detroit as its site, the project aims to develop new scenarios and new typologies for the emerging fallow cityscapes. The interventions propose more imaginative and public ways of using or mis-using the suburban forms, creating shared structures and photovoltaic systems--providing light for spaces of encounter even as the city infrastructure retreats.

        <p>A fallow season creates an interruption where unusual uses and forms can flourish. The exhibition presents a full-scale installation of a Solar Thicket structure prototype.

        <p>Deploying strategies found in fairytales, Solar Thickets can overgrow abandoned houses facilitating their enchantment and transformation. They can connect between houses, urban meadows, span over streets and interweave a new layer of public space into the existing suburban landscape.

