<div class="span50" id="henderson">
    <h2>Aaron Henderson | Midway</h2>

    <p class="small">
        <b>January 25 - February 19, 2010</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Henderson-Midway.pdf" target="_blank">Download announcement (pdf) ></a>
    </p>

        <p>    Aaron Henderson's videos and installations attempt to break down the ways that humans move. Recent projects examine the personal, cultural and political ramifications of all action, from intimate gestures to displays of super-human acrobatics. Well acquainted with movement, he threw himself into walls and off of platforms for STREB Extreme Action, an acrobatic performance company from 2002-6. He also designs projections for this and other projects which have been presented at Lincoln Center; ICA Boston; Wolf Trap, Vienna, Virginia; Bumbershoot, Seattle, Washington; the Oriente Occidente Festival, Verona & Trento, Italy; the Festival Paris; and many other theaters, colleges, and festivals across the country. These projects were aided by NYSCA and Creative Capital MAP grants. Currently, Aaron is a visiting artist in the McIntire Department of Art at the University of Virginia.


