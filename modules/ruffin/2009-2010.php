    <h2>Tim Davis | My Life in Politics</h2>

    <p class="small">
        <b>September 25 - October 23, 2009</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Davis-Politics.pdf" target="_blank">Download announcement (pdf) ></a>
    </p>

    <p>The Photography Program, the McIntire Department of Art and the Bridge PAI welcome Tim Davis, for a residency September 23-27 in conjunction with an exhibition of his work in Ruffin Gallery, My Life in Politics, September 25-October 23.

    <p>Tim Davis is an artist and writer living in Tivoli, New York, and teaching photography at Bard College and Yale University. His latest show, called The New Antiquity, will be at Greenberg Van Doren Gallery in New York, Sept-October, 2009. He is the author of five books of photographs: The New Antiquity (Damiani, 2009), My Life in Politics (Aperture, 2006), Illuminations (Greenberg Van Doren Gallery, 2006), Permanent Collection (Nazraeli Press, 2005), and Lots (Coromandel Express, 2002). He is also the author of two books of poems: American Whatever (Edge Books, 2004), and Dailies (The Figures, 2000). His photographs are in the collections of the Guggenheim, Metropolitan, Whitney, Hirshhorn, Walker, High and many other museums, and he has had solo exhibitions in New York, London, Los Angeles, Chicago, Brussels, Geneva, Atlanta and Miami. Davis was a Discovery Award finalist at the 2004 Arles Photography Festival and was the Joseph H. Hazen Rome Prize Fellow in residence at the American Academy in Rome in 2007-08. He is represented by Greenberg Van DorenGallery (NY).

</div>

