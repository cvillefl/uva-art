    <h2>Untitled<br><small>A Mid-Atlantic MFA Exhibition</small></h2>

    <p class="small">
        <b>July 9 - August 13<br>August 22 - September 16</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Untitled-announcement.pdf" target="_blank">Download announcement (pdf) ></a><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Untitled-catalogue.pdf" target="_blank">Download catalogue (pdf) ></a><br>
        <a class="orange" href="http://untitledexhibition.com/" target="_blank">Visit exhibition website ></a>
    </p>

    <p>    Untitled is a unique traveling exhibition of mfa work from around the mid-Atlantic region that pairs up-and-coming artists with emerging scholars, critics and curators. The exhibition emerged from our shared realization that while the art-world offers an increasingly robust support system for emerging artists, there was no substantial framework in place to connect these early career artists with peers engaged in critical or curatorial practice. We recognized that our position within the University of Virginia's (U.Va.) Graduate Program in the History of Art and Architecture enabled us to create singular moments of interpretive dialogue between young artists and scholars.

    <p>The exhibition catalogue explores these invisible currents in the context of each artist's work in interpretive essays written by seven art history graduate students at U.Va. from a broad rangeof academic specializations. For example, a graduate student specializing in medieval German culture discusses its re-examination in contemporary painting, and an emerging scholar investigating Byzantine icons as sites of performance and transformation explores the work of an artist situating the modern home in parallel terms.
