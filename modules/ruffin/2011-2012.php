    <h2>Samira Abbassy<Br><small>First annual Painter-in-Residence</small></h2>

    <p class="small">
        <b>April 1 - April 29, 2012</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/events/downloads/SamiraAbbassy.pdf" target="_blank">Download poster (pdf) ></a><br>
        <a class="orange" href="http://www.virginia.edu/uvatoday/newsRelease.php?id=17842" target="_blank">UVA Today | In the news ></a><br>
        <a class="orange" href="http://www.samiraabbassy.com/" target="_blank">More information on her work ></a>
    </p>

        <p>    Ms Abbassy is an Arab-Iranian artist who will be in residence and creating her own work from April 1 - 29, 2012 in Studio 323 at UVa's Painting department. She will present a public lecture on her work Wednesday, April 4 at 5:30 in Campbell Hall, Room 158.

        <p>Samira Abbassy is a painter whose work deals with women, war and identity. Born in Ahwaz, Iran, she moved to London as a child where she eventually attended the Canterbury College of Art.

        <p>In 1998, she moved to New York to help set up the Elizabeth Foundation for the Arts. Her work is shown internationally and has been acquired for private and public collections including the British Government Art Collection and The British Museum. She was awarded a Joan Mitchell Painting/Sculpture Award in 2010 and a Yaddo Residency in 2006.

        <p>In a review of her exhibition at the Anthony Giordano Gallery at Dowling College, NY, critic Benjamin Genocchio of The New York Times writes; 

        <p><i>Ms. Abbassy has said in an artist's statement that she uses self-portraiture as 'a way of defining myself in a constantly shifting cultural context.' Perhaps, then, these pictures are best approached as the embodiment of psychic, spiritual and emotional states. They are about the uncomfortable experience of trying to mold an identity from life's events and influences.</i>

        <p>At the end of her UVa residency, Ms. Abbassy will have an Open Studio on Friday, April 27 from 5:30 - 7:30 in Ruffin Hall, Studio 323. The public is invited to both her lecture and open studio.

