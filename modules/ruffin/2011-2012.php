<div class="span50" id="burckhardt">
    <h2>Tom Burckhardt<br><small>The Brooks Natural History Museum, c 1900; A Creative Interpretation</small></h2>

    <p class="small">
        <b>February 24 - March 30</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/resources/grant_achievements/big_bang/index.php#2011_12_visual" target="_blank">More Information ></a><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/burckhardt.pdf" target="_blank">Download Poster (pdf) ></a><br>
        <a class="orange" href="http://www.virginia.edu/uvatoday/search/news.html?cx=017593750330628318309:yj9lm2ikikq&q=Burckhardt&sa=Search&cof=FORID:11" target="_blank">UVA Today | In the news ></a><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/www.c-ville.com/Article/Reviews/UVAs_Brooks_Museum_resurrected_through_cardboard/?z_Issue_ID=11800503124116385" target="_blank">C-Ville | In the news ></a><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/events/2011-2012.php#artsboard" target="_blank">Artist in Residency></a><br>
    </p>

        <p>    This year, 2011 - 12, The University of Virginia Arts Board presents New York artist, Tom Burckhardt.

        <p>All school year, students have been working under artist Tom Burckhardt's direction to construct an artistic replica of the once Brooks Hall Natural History Museum, ca. 1900 out of nothing but brown corrugated cardboard, black paint, their research and imagination. This group of students, known as "The Cardboard Company", include Bridget Bailey, Hannah Barefoot, Marie Bergeron, Susannah Cadwalder, David Cook, Carmen Diaz, Shiry Guirguis, Margaret King, Brendan Morgan, Agnes Pyrchia, and Cherith Vaughan. Their creations so far include a wooly mammoth, a glyptodon, a doodoo bird, a squid and more. The entire Ruffin Gallery will be transformed in February into the Natural History Museum with a fantastic bent.

        <p>The installation, titled "The Brooks Natural History Museum, c 1900; A Creative Interpretation" will be on view at the Ruffin Gallery from Feb 24 - March 30, 2012. The opening reception will be Final Friday, February 24, 2012 @ 5:30. There will be a gallery talk with Tom Burckhartd and The Cardboard Company before the reception @ 4:30.

        <p>Tom Burckhardt was born in New York City where he presently resides. He has been the recipient of such prestigious art awards as The Guggenheim Foundation Grant, the Joan Mitchell Foundation Grant, Pollock-Krasner Foundation Grant and the New York Foundation for the Arts. His work has been exhibited international and has been reviewed in The New York Times, Art in America and The Village Voice. He is represented by Pierogi Gallery in Brooklyn, NY, where his last solo exhibition, "Louder Milk" was installed this April 2011.

        <p>The University of Virginia Arts Board is a student-run board at the University of Virginia whose mission it is to feature an outstanding artist, attraction, performance, or exhibition each year in a three-year rotation that includes music, visual arts, and drama.

