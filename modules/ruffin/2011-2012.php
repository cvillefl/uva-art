<div class="span50" id="lundgren-rothman">
    <h2>If There Be Such Space: Michael Lundgren and Aaron Rothman<br><small>a collaborative installation of photography</small></h2>

    <p class="small">
        <b>January 27 - February 17, 2012</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/events/2011-2012.php#lundgren" target="_blank">More Information ></a><br>
        <a class="orange" href="http://places.designobserver.com/feature/if-there-be-such-space-photographs/32258/" target="_blank">In the News ></a>
    </p>

        <p>    The Photography Department in Studio Art will mount an exhibition of contemporary photography in January and February 2012 in Ruffin Gallery featuring a collaborative installation by Michael Lundgren and Aaron Rothman. Lundgren and Rothman have spent the past decade engaged in a common photographic exploration of landscape. Sharing ideas, experiences and experiments, they have each created unique bodies of work investigating the complex connections between photography, perception, and the space of the natural landscape. At times, their work has been very similar in approach and process and at other times has diverged to opposite ends of a continuum representing a shared vision of photography as an essentially transformative medium. Occasionally they discover that they have each taken photographs from virtually the same vantage point in some vast landscape; these twin images reveal overlapping visions, but also very distinct sensibilities.

        <p>Lundgren's photographs express a visceral connection to a primal landscape. The deep history of our instinctual human consciousness is embedded in images that combine a profound sense of mystery with the clarity of desert light. Rothman's photographs immerse the viewer in spaces that are both anonymous and highly specific. The landscape becomes a place of presence--a perceptual field anchoring a sense of basic existence. Whether pushing the edges of photographic processes to create obviously altered images, or creating images that are apparently clear windows onto the space of the world, both Rothman and Lundgren are acutely aware of the gap between the photograph and the thing it pictures. In their work, this gap becomes a potent metaphor for the limits and possibilities of our perceptions and capacity to understand our place as individuals within the space of the natural world. Additionally, we are planning to bring both artists to Grounds for the installation process and a public lecture during the Ruffin exhibition.

        <p>Michael Lundgren is a faculty associate at Arizona State University. He has shown his work in numerous solo and group shows including Photo 21 in Tokyo and the History of Photography at the Victoria & Albert Museum in London. He has worked on collaborative projects for the city of Phoenix and on the historic Third View project documenting the American West. Radius Books published his book, Transfigurations, in 2008.

        <p>Aaron Rothman is a project specialist for the Urban Research Laboratory in the School of Architecture at Arizona State University. He is the photography editor for Places journal and received a Professional Development Grant from the Arizona Arts Commission for his work in 2008. His recent solo exhibits include the Cheekwood Museum of Art in Nashville and the Gitterman Gallery in NYC.

</div>

