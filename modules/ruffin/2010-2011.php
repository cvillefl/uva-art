    <h2>Katie Baines | recent painting</h2>

    <p class="small">
        <b>August 25 - September 17, 2010</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Baines-recent_paintings.pdf" target="_blank">Download announcement (pdf) ></a>
    </p>

    <p>We are pleased to introduce our new visiting faculty member for the 2010-2011 academic year, Katie Baines.

    <p>Katie Baines' paintings share the playful investigation of mapping strange landscapes with the use of decidedly psychedelic colors, patterns, and contours. Familiar forms, such as grass bordering stylized black and blue pools, buffer hot red and yellow-orange striations that could be unsuccessful Venetian blinds, which open up to delicate linear designs. Or an open geode of translucent purples is immersed with foreign shapes that spill out of the nodule. Her works act as records of what she has "brought back" to us to explore and fascinate over.

    <p>Baines earned her MFA from The Ohio State University in 2006. She currently resides in Richmond va and also teaches in the department of art at Virginia Commonwealth University.

