<div class="span50" id="painting-on-paper">
    <h2>Painting on Paper<br><small>Megan Marlatt, curator</small></h2>

    <p class="small">
        <b>September 24 - October 22, 2010</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Painting_on_Paper.pdf" target="_blank">Download announcement (pdf) ></a>
    </p>

    <p>Painting on paper is a much more accessible creative practice than painting on canvas and an important component to the painting program here at U.Va. Highly flexible, water medium on paper takes many forms and directions; from creating studies for larger works as in Judith Linhares gouaches, casual paintings that open the mind to the creative process as in Elena Sisto mixed media pieces, or highly refined works that are a serious end into themselves as in Kathy Muehlemann's watercolors. Not only can a young student use water media on paper to more easily access painting's tradition and discipline, but the approach can be as simple as clearing off the kitchen table for an evening's work session.

