<div class="span50" id="field-notes">
    <h2>Field Notes<br><small>Dean Dass, curator</small></h2>

    <p class="small">
        <b>October 29 - November 26, 2010</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Field_Notes.pdf" target="_blank">Download announcement (pdf) ></a><br>
        <a class="orange" href="http://www.virginia.edu/arts/downloads/Field_Notes-catF.pdf" target="_blank">Download catalogue (pdf) ></a>
    </p>

        <p>Field Notes is a collaborative group based in upstate New York's Hudson Valley region. Visual artists Robert Capozzi, Lorrie Fredette, Dylan McManus, Laura Moriarty, and Jill Parisi comprise this collective. The artists focus on concepts and issues that relate to the earth and its inhabitants with regard to the temporal, structures, systems, and resources.

        <p>For Ruffin Gallery the group will create a new work through a dynamic installation process, interacting with each other for a compressed duration with guidelines, random sequences and original components. The members are inspired by the geography of the Charlottesville region. Hailing from the Shawangunk Ridge and Catskill Mountain Range, this related yet unique location provides new topics to investigate and countless ways to respond visually.

