<div class="span50" id="yi">
    <h2>Yi Sheng<br><small>The Miraculous Mandarin</small></h2>

    <p class="small">
        <b>April 1 - 29, 2011</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Yi_Sheng-Miraculous_Mandarin.pdf" target="_blank">Download announcement (pdf) ></a>
    </p>

        <p>    These are the kinds of experiences that nudge a kind of homesickness in me. New Jersey is where I grew up, but it's not home.

        <p>The question of where that place is and what my fluctuating relationship to "home" might be are the immediate issues that arise. This is the core of my work - the complexities of how we situate our sense of self. What I put forth are the complications and contradictions of the psychology of dislocation. The experience of migration seems to leave me feeling as if I'm constantly straddling two shores, whether it is a physical separation of places, the mental and cerebral remapping of psyches, the linguistic and communicative misfires, or cultural divergences. There's a feeling of permanent impermanence, of never arriving at the destination because both beaches look close but I'm still treading water right in the middle of it all.

        <p>While my larger concerns frequently touch on gender, race, and class, my overall practice is based on personal experiences, many of which result in projects that do not always fit so easily within these thematic parameters. I embrace a mix of video, performance, photography, and object making. Employing a broader set of tools with which to communicate enables an added complexity, sparking conceptual and material connections within each piece and between different works. This way of creating feels natural, perhaps because it reflects the exhaustive range of how our daily experiences are comprised.
        <Br>
        --Yi Sheng
