<div class="span50" id="pecchio">
    <h2>Pamela Pecchio | Habitation</h2>

    <p class="small">
        <b>October 31 - November 28, 2008</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Pecchio-Habitation.pdf" target="_blank">Download announcement (pdf) ></a>
    </p>

    <p>Habitation is an exhibition of work by Pamela Pecchio, selected from a number of photographic projects she has undertaken 
    during the last several years. The works presented here represent what I believe to be the central and most compelling focus and direction of her work to date, 
    that of inhabited space and how we make it our own. Pecchio's work confronts how we define the spaces we inhabit, and in turn, what those spaces say about the 
    human presence within them. Wading through the visual detritus in which we dwell, she documents those objects we choose to covet, or more tellingly, what has escaped removal. 
    While these photographs function as visual explorations of the physical sequencing of objects that are selected to fill our domestic environments, they also isolate 
    and expose those physical marks we leave behind. Felt as much as seen, these marks result from human presence and our patterns of use and Pecchio 
    presents them to us as mysterious abstractions rather than moments of clarity. These are the liminal spaces, the thresholds and boundaries between visual perception 
    and physical presence that ask more questions than provide us with answers. Whether these marks are the result of accident, repetition or intent--they 
    have been left behind. With each work Pecchio presents us with these often overlooked domestic mysteries, ultimately capturing the sublime state of being there.<br>
-- Lauren Ryan</em></p>

