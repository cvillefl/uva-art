<div class="span50" id="landscape">
    <h2>Landscape</h2>

    <p class="small">
        <b>January 13 - Feruary 20, 2009</b><br>
    </p>

        <p>From January 13 through February 20, 2009, the McIntire Dept of Art will present a multi-media exhibition entitled <em>Landscape</em> in the Ruffin Gallery. 
        The exhibition was organized by independent curator <b>Emily Schroeder</b>, founder of Chicago's Normal Projects, in conjunction with the New Media concentration in the Art Department and seeks to examine the idea of landscape across mediums. A reception will be held January 30 from 5:30 - 7:30 in the gallery.<br><br>

        In <em>Landscape</em>, Schroeder has gathered twelve contemporary artists whose practices range from experimental video and animation to sculpture and drawing.  Approaching landscape and nature as conventions that are constantly evolving, she has drawn together a group of works in which traditional and new media peacefully coexist, sometimes within the same piece.  New York based sound artist Christian Toscano has pressed an edition of vinyl records for the occasion while LA centered Marcus Civin contributes a piece of original fiction to be printed and given away to gallery visitors.  North Carolina resident Jerstin Crosby lends his take on environmental activism in the video <em>E.L.F.</em> while New York based Devlin Shea contributes a hand drawn, frame by frame animation called <em>Deer and Lynx</em>.<br><br>

        Other artists whose work will be included in the exhibition are Joe Denardo (New York),  Peter Burr (Portland, OR), Jesse Avina (Chicago), JD Walsh (New York), Conrad Ventur (New York), Luke Dowd, (London) Dawn Blackman (New York), Kristopher Benedict (Berlin), and Sarah Morgan (New York).
        </p>


