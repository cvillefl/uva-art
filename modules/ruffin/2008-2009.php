<div class="span50" id="vertanen-kunc">

    <h2>The Indeterminate Edge<br><small>
Two person exhibition and residency<br>
Annu Vertanen and Karen Kunc</small></h2>

    <p class="small">
        <b>September 22 - October 24, 2008</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/Vertanen_and_Kunc-Indeterminate_Edge.pdf" target="_blank">Download announcement (pdf) ></a>
    </p>

    <p>We are honored to bring the work of Karen Kunc and Annu Vertanen together in our new Ruffin Hall Gallery. These are two artists for whom the woodcut has remained at the center to their studio practice.

    <p>In his essay entitled "The Storyteller" Walter Benjamin distinguishes between two archaic modes. There is the one who stays home, lives within a family 
    or village nexus and so knows all those stories, family histories, all of those particularities. But there is another who goes to sea, or otherwise travels, and returns home from 
    those odysseys with stories of adventures, and knowledge gained of others.<sup>1</sup> It seems Karen Kunc and Annu Vertanen embody both modes of Benjamin's storyteller. 
    Kunc grew up in Nebraska and is the great, great granddaughter of Czech immigrants. Those stories and those prairie spaces fill her work. Today she holds the Cather Chair at 
    the University of Nebraska, Lincoln. Yet few of us have traveled so far and to so many places. She travels for inspiration, but also because the quality 
    of her work demands it; she has held 90 solo exhibitions and conducted workshops around the world. The Southern Graphics Council has recently 
    recognized her achievement with their Printmaker Emeritus Award. In 1986 Kunc traveled to Finland under the 
    auspices of the Arts America Program of the U.S. Information Agency, where she taught a color woodcut workshop in Jyv&auml;skyl&auml;. A young Finnish painter attended that workshop and, astonishingly, scarcely ever painted again. Annu Vertanen has since become a celebrated printmaker, having won prizes in numerous international exhibitions. Vertanen likewise insists on living in a small isolated city, Imatra, situated along the river Vuoksi, on the Russian border. The word homely figures prominently in her work. From this particularity she has lived for months in India (to study yoga), Japan and the United Sates. We imagine Kunc and Vertanen woodcuts even today in shipping crates and headed to countless exhibitions.

    <p>Kunc has imagined into being a kind of hybrid woodcut. She uses European oil-based inks, etching presses, and a modified reduction technique, yet her veils of translucent inks seem to hover over and within delicate Asian papers. The title of this exhibition, also the title of one of her prints, The Indeterminate Edge, seems particularly apt. You will be forgiven if you think her work was printed in 18th century Japan using traditional Ukiyo-e techniques. Where is the surface? Where is the image? We're not sure. Perhaps we should also talk about the insights of Color Field painting. In addition to this relationship between surface and image, we remember that throughout the '80s -- when both of these artists came to maturity -- the woodcut flourished as a medium of large-scale printed works. This was very much like the explosion of scale we see happening in contemporary photography.

    <p>Vertanen has followed this insight and taken it into new realms. After all, Ukiyo-e printers knew nothing of plywood. Her woodcuts venture into large scale installation and tangle with video. The sublime spaces are still there. Here Vertanen exhibits this kind of installation in a work entitled, roughly speaking, "The Day of Absence". We are unable to adequately translate this concept, even with the artist's help. In what sense is the word Absence used? Vertanen writes "My goal is to offer an opportunity to watch what happens when one watches. That is the space within that opens up towards the world (or how we see the world)." Conversations with artists often end like this, for "One always arrives at something which one can no longer depict."<sup>2</sup> Now we are starting to get somewhere. On the other side of Absence we find Presence. How often are we really present? Perhaps Vertanen would engage the trials of self-consciousness.

    <p>Kunc has been working in a square format lately, yet the works are often diptychs. These two absolute forms talk to each other. Often she doesn't seem to be describing anything as such, yet our associations wander and blossom. I find myself thinking about time, about the seasonal rhythm of flood plains along the Missouri, about those same fertile, furrowed fields, about vast skies and the approach of distant storms. Kunc herself writes about the "interpretation and contemplation on larger issues of the eternal life struggle, of endurance and vulnerability, growth and destruction. My hope is that these larger concepts are provoked by viewing my work with a poetic and intelligent sense of wonder."

    <p>Wonder...that's not a bad place to end. Our new gallery is filled with woodcuts, that venerable form, today printed in bright and pure colors. Welcome to the exhibition.<br>
    &mdash;Dean Dass

    <p class="credit">
    1. Pp. 84-85. Benjamin,Walter, "The Storyteller", in Illuminations, Schocken Books, New York, 1968.<br>
    2. P. 144, Dieter Roth in an interview with Felicitas Thun, in her exhibition catalog of Roth's work, Prints and Books 1 1949-1979, Graphische Sammlung Albertina, K&ouml;ln, 1979.
    </p>

