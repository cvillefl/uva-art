<div class="span50" id="end-of-language">
    <h2>The End of Language <Br><small>Justin Quinn, Suzanne Silver, Adam Wolpa, Lauren Catlett<br>Four person exhibition</small></h2>

    <p class="small">
        <b>March 1 - March 29, 2013</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/end-of-language-catalogue.pdf" target="_blank">Download catalogue (pdf) ></a><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/images/2012-2013/end-of-language-ecard.jpg" target="_blank">View invitation ></a><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/end-of-language-poster.pdf" target="_blank">Download poster (pdf) ></a>
    </p>

        <p>    We are a Sign, unreadable.
        <br>
        - Friedrich H&ouml;lderlin

        <p><b>Introduction</b><br>
        We are grateful that these four artists - Lauren Catlett, Justin Quinn, Suzanne Silver and Adam Wolpa - have allowed us to gather their works together in this exhibition, and we are grateful likewise for Monica's McTighe's supporting - and supportive - essay on the works. Her essay helps establish the broader context for artist's working in this genre. The artists have provided us a rich installation, one slow to reveal, or at least to reveal fully. The installation is not entirely formless. The end is not the end. 

        <p>Each of these artists has thought deeply about their works, working over a long period of time, and to bring them together into this collaborative venture has, we hope, enriched the individual works. We sense the diligence and devoted study of these artists. I hope even that the artists might gain something from the collaborative installation of their works. Perhaps they have found a new friend, or a new dimension to an old friend.

        <p>These artists are not dismayed by the everyday failure of language, just the opposite. In the ruins they see an opening and an opportunity. Let's here reprise Walker Percy's wonderful title: Love in the Ruins.

        <p>The first version of The End of Language was a portfolio a number of us put together around 2001. Wolpa and Quinn were in graduate school at Iowa at that time, and together with fellow graduate student Alan Harmon, provided the organizational principles guiding that portfolio. In the intervening years I have watched their works continue to develop ideas nascent in that early portfolio, and have long considered reanimating the project. Suzanne Silver had left Charlottesville for Columbus by the mid '90s, yet I have continued to follow her installations. Two years ago she visited again, and our conversation in Ruffin that day provided the impetus to go forward. She was, after all, already familiar with, and enthusiastic about, Quinn's work. Lauren Catlett, the fourth in our quartet, is a recent graduate of the Studio program at Virginia, and from the first works I ever saw her realize, it was clear she could join this group.

        <p>Wolpa closes the 2001 prospectus with the following sentence, as good today as then:
        "Please do not respond, these words have already begun to look only like horizons."

        <p>Welcome to the exhibition!

        <p>Professor Dean A. Dass, Curator

