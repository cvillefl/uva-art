    <h2>Earth. Fire. Air. Water.</h2>

    <p class="small">
        <b>August 31 - September 21</b>
    </p>

    <p>    <i>Earth, Fire, Air, Water</i> is the first in a two-part series of exhibitions featuring recent works by the University of Virginia Studio Art Faculty. Spanning painting, sculpture, photography, printmaking, and video, the diverse body of works on display each engage with the legacy of the landscape genre. Very few of these works, however, offer traditional depictions of the earth, sea, or sky. Some works isolate and enlarge particularly evocative facets of the terrain to create moving depictions of the spirit of a place. Others works instead offer clues to the experiences or sensations associated with the four elements through physical artifacts or painterly abstractions. Whatever their technique or approach, the works share a common interest in capturing the subtle beauty that can be found in otherwise unremarkable corners of the world and a willingness to explore the basic substances that compose it.

        <p><i>Earth, Fire, Air, Water</i> offer students a rare opportunity to see and learn from their professors' mature work. For the university at large, this exhibition is a chance to share in the creative research that members of the university community have produced in the last two years both on and off grounds. As a showcase of some of the best new artwork from Central Virginia and elsewhere, Earth, Fire, Air, Water is a must-see event for everyone in the Charlottesville area.


</div>

