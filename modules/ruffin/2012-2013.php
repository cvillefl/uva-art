<div class="span50" id="heffernan">
    <h2>Julie Heffernan<br><small>Second annual Painter-in-Residence</small></h2>

    <p class="small">
        <b>October 2 - 26, 2012</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/heffermanposter.pdf" target="_blank">Download poster (pdf) ></a>
    </p>

       <p>    The Studio Program at the University of Virginia's McIntire Department of Art presents its second annual Lee-Ellen Fleming Painter-in-Residence, <a href="http://www.markmooregallery.com/artists/julie-heffernan/" target="_blank">Julie Heffernan</a>.

        <p>Ms Heffernan is a New York artist and Associate Professor of Art at Montclair College in NJ who will be in residence and creating her own work from October 2-26, 2012 in Studio 323, Ruffin Hall. She will present a public lecture on her work Wednesday, Oct 3 at 5:30 in Campbell Hall, Room 153. 

        <p>Julie Heffernan has been the recipient of many awards, including a National Endowment for the Arts grant, (1995), and the Lila Acheson Wallace Reader's Digest Artist at Giverny, (France.) Her work is included in many national and international collections, including the Columbia Museum of Art (Columbia, South Carolina), the Virginia Museum of Fine Art (Richmond, Virginia), and the Zabludowicz Art Trust (London, United Kingdom). Ms. Heffernan's paintings have been featured in solo exhibitions at institutions such as the Lux Art Institute (Encinitas, California), the John Michael Kohler Art Center (Sheboygan, Wisconsin), and the Mint Museum of Art (Charlotte, North Carolina) as well as prestigious galleries like POWW, (New York, NY) and Catherine Clark Gallery, (San Francisco, CA.) Her work has been written about and published in Artforum, Art in America, Artnews, and 
        The New York Times.

        <p>David Cohen, art critic of The New York Sun, aptly describes Heffernan's art: "These paintings are a hybrid of genres and styles, mixing allegory, portraiture, history painting, and still life, while in title they are all presented 
        as self portraits."

        <p>At the end of her U.Va. residency, Ms. Heffernan will have an Open Studio on Final Friday, October 26 from 
        5:30-7:30 in Studio 323, Ruffin Hall. The public is invited to both her lecture and open studio.

