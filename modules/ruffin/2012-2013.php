<div class="span50" id="new-natural-history">
    <h2>New Natural History<br><small>Faculty Exhibition</small></h2>

    <p class="small">
        <b>September 26 - October 22</b><br>
        <a class="orange" href="http://www.virginia.edu/arts/visual_arts/museums/ruffin/downloads/newnaturalhistory.pdf" target="_blank">Download invitation/poster (pdf) ></a>
    </p>

    <p>    Odd ducks, exotic specimens, curious artifacts--the second and final installment of the University of Virginia studio faculty exhibition features an eclectic mix of objects that merge the real and the surreal. Combining figurative and non-representational modes, the works on display belie any straightforward reading, and instead playfully juxtapose the natural with the artificial, the recognizable with the strange. Many of these pieces present seemingly pedestrian or familiar objects in a new or unfamiliar light. Other works feature abstract forms that have been fashioned with uncanny intimacy. Still other pieces appropriate objects or imagery with established uses, changing their significance in the process and achieving highly personal results. While some works take their inspiration from the Ages of Exploration and Enlightenment, marked as they were by an incessant drive to discover, study, and categorize the knowable world, still others tackle unresolved questions of identity, authority, and belonging left in their aftermath. New Natural History offers a present-day cabinet of wonders where the spectral, the natural, and the post-colonial collide.

</div>

