                <h2><a href="<?php p('faculty'); ?>studio-art/36/william-wylie">William Wylie</a></h2>
                Professor and Associate Chair, Photography

                <br><br>

                William Wylie was on leave in the fall and in residence at ART342 in Fort Collins, 
                CO while shooting his documentary film Prairie, about the six-man football team at 
                a small rural school. He followed the team through the entire 2012 season. After 
                that he was awarded the Yale University Art Museum's Doran/LeWitt Residency in 
                Praiano, Italy on the Amalfi Coast. He produced a new series of photographs and 
                video pieces in Pompeii and on Mount Vesuvius. His work was featured in solo and 
                group exhibitions at <a href="https://www.yale.edu/" target="_blank">Yale</a>, <a href="https://www.joslyn.org/‎" target="_blank">The Joslyn Museum of Art</a> and the <a href="http://www.cartermuseum.org/" target="_blank">Amon Carter Museum</a>. 
                He ended the academic year teaching another of the bi-annual Study Abroad classes
                in Italy where his students made unique pinhole cameras to photograph in Rome, 
                Tuscany and Venice.
