		<h1>Program in Classical Art and Archaeology</h1>

		<p class="small">The McIntire Department of Art sponsors an interdisciplinary Program in Classical Art and Archaeology, leading to the Ph.D degree. Founded in the 1980s, the program aims to foster a thorough acquaintance with Greek and Roman art, it also allows students to acquire a broad 
understanding of ancient culture. Reading knowledge of Greek and Latin is encouraged, and competency in relevant modern languages is required. Credit is given for relevant courses offered by other departments such as Anthropology, Classics, History, or Religious Studies. Course work may also be taken in a relevant post-classical period, such as Medieval or Renaissance. At an appropriate stage in their graduate study, most students in the program will also participate in archaeological field work. Recent students have worked at Pompeii, 
<a href="http://en.wikipedia.org/wiki/Morgantina" target="_blank">Morgantina</a>, the Athenian Agora, Isthmia, Mycenae, Tel Dor (Israel), Porolissum (Romania), 
Atheniou (Cyprus), and Heraclea Sintica (Bulgaria). </p>

<p class="small">The curriculum is determined by the student's preparation, interests, 
and needs. Course work for the Ph.D. should be concentrated in the 
area of specialization in ancient art (sculpture, painting, architecture, 
or various period fields) and in the special field, as described above. 
The Ph.D. examination in the area of specialization takes place after the completion of course requirements. For dissertation requirements see Ph.D. Requirements. </p>

<p class="small">The program instructors include 
<a href="/faculty/art-history/6/anastasia-dakouri-hild">Anastasia Dakouri-Hild</a>, 
<a href="/faculty/art-history/7/john-j-dobbins">John Dobbins</a>, and 
Tyler Jo Smith. Students are encouraged to work closely with faculty in other departments such as 
<a href="http://www.virginia.edu/anthropology/" target="_blank">Anthropology, 
<a href="http://www.virginia.edu/classics" target="_blank">Classics and 
<a href="http://www.virginia.edu/history/" target="_blank">History. The 
<a href="http://www.virginia.edu/art/aia/" target="_blank">Archaeological Institute of America Charlottesville Society</a> is based at the University; most graduate students become involved as members and participate in local events. 
 </p>
