		<h2>Course requirements</h2>
		<p>
			The PhD degree in Art and Architectural History requires 42 graded credits. Students who are admitted with an MA in art or architectural history may transfer 12 graded credits. Graded credits are typically 
			completed through a combination of coursework and supervised research with the student's PhD advisor. Students are encouraged to take a 
			wide range of Art and Architectural History PhD courses (arah in the course catalogue), and they may also take graduate courses in other departments that contribute to their PhD thesis formation. All students 
			are expected to be ABD by the end of their third year of study. 
		</p>

		<div class="dots_hr"></div>

		<h2>Language requirements</h2>
		<p>
			Reading knowledge of at least two foreign languages is required. These languages should be appropriate to the dissertation field and determined in consultation with the students' PhD advisor.
		</p>

		<div class="dots_hr"></div>

		<h2>PhD examinations</h2>
		<p>
			Students will undertake a PhD examination prepared by three faculty members in their area of study. Exams should demonstrate the student's mastery of foundational primary sources, historiographical issues, and scholarship broadly related to the dissertation. The PhD examination includes both written and oral components. 
		</p>

		<div class="dots_hr"></div>

		<h2>Dissertation prospectus</h2>
		<p>
			Students will write a dissertation prospectus of no more than ten pages (excluding bibliography) that includes an abstract; a project statement outlining the project's scope, situating it historiographically, and 
			identifying its contributions; a program and schedule of research; and 
			a select bibliography. 
		</p>

		<div class="dots_hr"></div>

		<h2>Dissertation presentation</h2>
		<p>
			In the midst of advancing their research and writing, each student is asked to give a forty-minute talk based on the dissertation to an audience of faculty and graduate students. Neither an examination nor a defense, this is an occasion for students to share their scholarship in a formal venue, to learn how to engage with historians of art and architecture from different areas, and to practice the kind of presentation skills demanded by a job talk.
		</p>
