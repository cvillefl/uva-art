		<h1>Prospective Students</h1>

		<p>The PhD Program in Art and Architectural History enrolls approximately five students per year.  All students receive a five-year package that includes full-tuition remission, health insurance, and an annual stipend of $18,000. Students with either a BA or an MA in art history, architectural history, or related fields are encouraged to apply for the PhD program. Students who are admitted into the program with a BA can apply for an 
		'in process' MA on their way to the PhD degree.
		</p>
		<p>
		In addition to the baseline fellowship package above, PhD students may apply for Departmental and University grants for summer travel, 
		language study, prospectus research, and reimbursement for conference participation and publication expenses.</p>
