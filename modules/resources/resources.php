		<h1>Resources</h1>

		<p>The Distinguished Major Program in Studio Art is designed for majors who have a 3.4 GPA or better, have demonstrated exceptional engagement with their studio work, and who want to undertake an extended project under the supervision of a faculty advisor while participating in a year-long seminar with similarly motivated students from across studio disciplines. The <acronym>dmp</acronym> is ideal for students who are interested in understanding their work in the larger context of contemporary art. </p>
 
		<p>During the third year, potential students submit proposals for an ambitious thesis project involving written research as well as the development of the significant body of art work. In the spring of the fourth year, the finished research is submitted to the department and the artwork is presented to the public as an exhibition in Ruffin Gallery.</p>
		 
		<p>Successful completion of the Distinguished Major Program is a very effective preparation for graduate work, and many studio majors 
		who succeed in fulfilling the stringent demands of the program go on to distinguish themselves in outstanding graduate schools.</p>
