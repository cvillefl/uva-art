        <p class="small">Sarah Betzer's research examines eighteenth and nineteenth-century European art with particular emphasis on the intersections of art theoretical 
        debates and artistic process in French painting. Her book, <a href="http://www.psupress.org/books/titles/978-0-271-04875-8.html" target="_blank"><i>Ingres and the Studio: Women, 
        Painting, History</i></a> (Penn State University Press, 2012), focuses on J.-A.-D. Ingres, a critical figure of the modern era and an artist celebrated in his lifetime and beyond as one of the most esteemed portraitists of all time. <i>Ingres and the Studio</i> situates this essential aspect of Ingres's oeuvre in the context of his studio practice and his training of students, positing that female portraiture functioned for these artists as a privileged model of ambitious painting itself.</p>
 
        <p class="small">Betzer joined the University of Virginia faculty in 2007. The recipient of grants from the Kress Foundation and the Getty Research Institute, her work has 
        appeared in <i>Art History</i>, <i>The Art Bulletin</i>, and caa.reviews. At UVA, her undergraduate teaching has included lecture courses on modern European art, on Paris in the nineteenth century, and 
        seminars on such topics as Art History's Feminisms and Ingres. Her graduate seminars have included the Modernity of Ancient Sculpture, Theory and Methods in the Visual Arts and Manet and the Modern.</p>
 
        <p class="small">Betzer will spend 2014-15 at Institute for Advanced Study in Princeton, where she will be working on her current book project, <i>Picturing Antiquity and the Body after Archaeology</i>.  Betzer spent the 2010-11 academic year as an Ailsa Mellon Bruce Senior Fellow at the Center for Advanced Study in the Visual Arts at the National Gallery of 
        Art. She is a member of Editorial Board of <i>The Art Bulletin</i>. </p>

