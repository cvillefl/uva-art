        <h2><i>Picturing Antiquity and the Body after Archaeology </i>
(in progress) 
 </h2>
 
        <p>This book examines artistic encounters with and pictorial responses to ancient figural sculpture from the discoveries of Herculaneum and
 Pompeii in the mid eighteenth century to the late nineteenth century. Focusing on episodes of transformation and vivification, the project reads against the grain of the longstanding and seductive rhetorical marriage of antiquity, sculpture, and morbidity, the origins of which have been traced to the 17th-century founding of the French Academie desBeaux-Arts 
and to the rhetorical patterns attendant to the paragone. To this end, the project unites lines of inquiry that have been examined almost exclusively in isolation: on the one hand, the rise of eighteenth-century philosophical inquiry emphatically centered on the matter of bodily sensations and aesthetic experience, and on the other the artistic culture of the Grand Tour which was defined by a widespread enthusiasm for antique 
sculpture. The study's animating argument is that by the late-eighteenth century these two histories were powerfully convergent and would remain so well into the nineteenth century. Organized to examine a series of individual artists' encounters with antique figural sculpture and their pictorial responses in painting, engraving, and photography, the book moves between a wide range of destinations-among them Florence, Rome, Naples, Dresden, and Paris-and spans roughly a century: from the golden age of the Grand Tour (1760-70) to the second half of the nineteenth century. Emphasizing ancient sculpture's centrality to vital debates-at once philosophical, aesthetic, antiquarian, archaeological, touristic, museological and art historical in nature-the book offers a new account of the persistence of antiquity in the modern period.
</p>

